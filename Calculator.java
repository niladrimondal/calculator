package calculation;

public class Calculator {
	int num1, num2;
	//<accessmodifier> <returntype> name of the method(inputparameter)
	int sum(int num1, int num2) {
		return num1+num2;
	}
	
	int substruct(int num1, int num2) {
		return num1-num2;
	}
	
	int multiply(int num1, int num2) {
		return num1*num2;
	}
	int division(int num1, int num2) {
		return num1/num2;
	}
	
}
